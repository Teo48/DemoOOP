package junit;

public class FloatCalculator {

	public float add(float first, float second) {
		return first + second;
	}

	public float multiply(float first, float second) {
		return first * second;
	}

	public float divide(float first, float second) {
		return first / second;
	}

	public boolean isNegative(float num) {
		return num < 0;
	}
}